-- Table Schema:
-- Table: cocoa_logs
--   log_id: INT
--   break_id: INT
--   cocoa_id: INT
--
-- Table: break_schedule
--   break_id: INT
--   location_id: INT
--
-- Table: cocoa_types
--   cocoa_id: INT
--   cocoa_name: VARCHAR
--
-- Table: locations
--   location_id: INT
--   location_name: VARCHAR
--

-- My Solution:

select log_id, break_id, cocoa_id,cocoa_name, location_name
from cocoa_logs
left join break_schedule using (break_id)
left join cocoa_types using (cocoa_id)
left join locations using (location_id)
